import 'package:flutter/material.dart';
import 'package:plupool/core/utils/size_config.dart';
import 'package:plupool/features/home/presentaation/views/admin/widgets/app_drawer.dart';
import 'package:plupool/features/home/presentaation/views/admin/widgets/custom_app_bar.dart';
import 'package:plupool/features/services/presentation/views/admin/widgets/admin_drawer_service_view_body.dart';

class AdminDrawerService extends StatefulWidget {
  const AdminDrawerService({super.key});

  @override
  State<AdminDrawerService> createState() => _AdminDrawerServiceState();
}

class _AdminDrawerServiceState extends State<AdminDrawerService> {
 final GlobalKey<ScaffoldState> scaffoldkey = GlobalKey();
  @override
  Widget build(BuildContext context) {
    SizeConfig.init(context);
    return Directionality(
      textDirection: TextDirection.rtl,
      child: SafeArea(
        child: Scaffold(
          key: scaffoldkey,
          appBar: CustomAppBar(
            isSearch: false,
            onPressed: () {
              scaffoldkey.currentState!.openDrawer();
            },
          ),
          drawer: AppDrawer(),
          body: Padding(
            padding: EdgeInsets.symmetric(
              horizontal: SizeConfig.w(13),
              vertical: SizeConfig.h(15),
            ),
            child: AdminDrawerServiceViewBody(),
          ),
        ),
      ),
    );
  }
}